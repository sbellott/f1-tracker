import NextAuth from 'next-auth';
import Credentials from 'next-auth/providers/credentials';

/**
 * Edge-compatible auth configuration (no Prisma)
 * Used only for middleware authentication checks
 */
export const { auth } = NextAuth({
  session: {
    strategy: 'jwt',
    maxAge: 30 * 24 * 60 * 60, // 30 days
  },
  pages: {
    signIn: '/login',
    error: '/login',
  },
  providers: [
    // Credentials provider without authorize function for Edge
    // The actual authentication happens in the full config
    Credentials({
      name: 'credentials',
      credentials: {
        email: { label: 'Email', type: 'email' },
        password: { label: 'Password', type: 'password' },
      },
      // Edge runtime doesn't call authorize, it just validates JWT
      async authorize() {
        return null;
      },
    }),
  ],
  callbacks: {
    async jwt({ token, user }) {
      if (user) {
        token.id = user.id;
      }
      return token;
    },
    async session({ session, token }) {
      if (session.user && token.id) {
        session.user.id = token.id as string;
      }
      return session;
    },
  },
});
